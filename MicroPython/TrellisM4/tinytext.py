class TinyText:
    def __init__(self):
        pass

    def get_char_rows(self, c):
        uc = c.upper()
        if uc in _font_4_n:
           return _font_4_n[uc]
        return []

    def get_char_columns(self, c):
        rows = self.get_char_rows(c)
        nrows = len(rows)
        if nrows == 0:
            return

        ncols = len(rows[0])
        for col in range(0, ncols):
            yield tuple([rows[row][col] for row in range(0, nrows)])   
        
    def get_string_colums(self, str):
        for c in str:
            for col in self.get_char_columns(c):
                yield col
